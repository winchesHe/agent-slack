/** 供 LLM 生成候选规则时遵循的编写指南（Markdown 原文）。 */
export const AGENTS_RULE_WRITING_GUIDE = `# AGENTS.md 规则编写指南

你正在为一个长期运行的 AI agent 生成可追加到 \`AGENTS.md\` / \`system.md\` 的规则。
每条规则都会被未来的 agent 以全量方式加载进 context，因此必须**高密度、可验证、能立即执行**。

## 1. 好规则的标准

一条合格的规则，必须能直接帮助 agent 回答以下至少一个问题：

- 如何安装依赖 / 构建 / 跑测试 / 跑 lint？
- 目录结构与关键模块的职责？
- 命名、代码风格、错误处理、日志等约定？
- 哪些区域 / 文件 / 操作**不能**改动？（护栏）
- 提交 / PR / 完成前必须执行哪些检查？
- 与外部系统（DB / API / 第三方）交互的约束？

不要生成"提醒式"、"原则式"、"美德式"的话术（如"注重代码质量"、"保持良好沟通"），这类规则对 agent 无操作价值。

## 2. 核心设计规则

写**最小完整操作文档**：一条规则 = 一个可执行的操作约束，句子越短越好，信息密度越高越好。

**反例**：
> 在修改代码时应当注意遵循项目的代码风格，保持整洁。

**正例**：
> \`pnpm lint\` 必须通过；禁止 \`any\`，用 \`unknown\` + type guard 替代。

## 3. 编写原则 P0-P6

- **P0 Token 成本**：每个 token 都要为未来 agent 省决策时间。删掉所有修饰语。
- **P1 邻近性**：规则要贴近它生效的场景（"提交前…"、"改 env 时…"），不要孤立陈述。
- **P2 具体性**：给命令、文件路径、函数名、字段名；不要"某个配置文件"。
- **P3 护栏优先**：禁止类规则（Don't）> 建议类规则（Prefer）。护栏对 agent 价值更高。
- **P4 密度**：一行能表达的不要两行；可用短语就不用整句。
- **P5 训练对齐**：措辞要接近 agent 已掌握的常见技术语义（如 "use X instead of Y"），少造新术语。
- **P6 可验证性**：规则必须可被一条命令、一段 diff 或一条测试验证。不可验证的规则不要生成。

## 4. 决策规则

- **具体事实 > 通用指导**：优先提炼"这个项目用 pnpm / Node 22 / ESM"，而非"用合适的包管理器"。
- **短命令 > 长解释**：\`pnpm test\` 胜过"运行测试套件以验证变更"。
- **约束 > 愿望**：写"禁止 X"/"必须 Y"，不要写"希望"、"尽量"、"最好"。
- **来源证据充足才写**：一条规则必须能在提供的数据里找到 ≥2 次重复证据（反复出现的错误、用户反复纠正、反复使用的命令）。只出现一次的模式不是规则。

## 5. 自检清单

生成每条规则后，逐项自检：

- [ ] 规则是否能在不看任何上下文的情况下被 agent 立即执行？
- [ ] 规则是否给出了具体的命令 / 路径 / 文件名 / 字段名？
- [ ] 规则是否与现有 AGENTS.md / system.md 中的条目重复或冲突？若重复或冲突，直接丢弃不要输出。
- [ ] 规则措辞是否尽量短？有无冗余修饰？
- [ ] 规则是否属于护栏（Don't）、执行路径（How）、约定（Convention）之一，而非空泛倡导？
- [ ] 是否能指出至少 2 条数据证据支撑此规则？

未通过任一自检的候选规则，**不要输出**。

## 6. 输出格式

每条候选规则必须是一条独立的 Markdown 片段，尽量控制在 **1-3 行**内。
同一主题下的多条操作可用短列表呈现。

示例（两条完全合格的规则）：

\`\`\`
- 包管理用 \`pnpm\`；禁用 \`npm\` / \`yarn\`。
- 提交前必须 \`pnpm lint\` 通过；失败改根因，不用 \`--no-verify\` 绕过。
\`\`\`
` as const
